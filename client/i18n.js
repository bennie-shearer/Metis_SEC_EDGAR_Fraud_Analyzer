// I18n engine
// ---------------------------------------------------------------------------
const I18n = (() => {
    const SUPPORTED   = ['en', 'en-GB', 'fr', 'de', 'es', 'ja', 'zh', 'ko', 'pt'];
    const DEFAULT     = 'en';
    let   _locale     = DEFAULT;

    /** Resolve a locale string to a supported locale, falling back to 'en'. */
    function resolve(locale) {
        if (!locale) return DEFAULT;
        if (SUPPORTED.includes(locale)) return locale;
        // Try base language (e.g. 'fr-CA' → 'fr')
        const base = locale.split('-')[0];
        if (SUPPORTED.includes(base)) return base;
        return DEFAULT;
    }

    /** Return a translated string, falling back to en then the key itself. */
    function t(key) {
        const table = I18N_TRANSLATIONS[_locale] || {};
        if (table[key] !== undefined) return table[key];
        const en = I18N_TRANSLATIONS['en'] || {};
        return en[key] !== undefined ? en[key] : key;
    }

    /** Set the active locale and persist it to localStorage. */
    function setLocale(locale) {
        _locale = resolve(locale);
        try { localStorage.setItem('locale', _locale); } catch (_) {}
        document.documentElement.lang = _locale;
        applyToDocument();
    }

    /** Return the active locale. */
    function getLocale() { return _locale; }

    /** Return the list of supported locales. */
    function supported() { return [...SUPPORTED]; }

    /**
     * Translate all elements in the document that carry data-i18n attributes.
     *
     * Attribute variants:
     *   data-i18n="key"               → element.textContent = t(key)
     *   data-i18n-html="key"          → element.innerHTML   = t(key)
     *   data-i18n-placeholder="key"   → element.placeholder = t(key)
     *   data-i18n-title="key"         → element.title       = t(key)
     *   data-i18n-value="key"         → element.value       = t(key)  (for options)
     */
    function applyToDocument() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            el.textContent = t(el.dataset.i18n);
        });
        document.querySelectorAll('[data-i18n-html]').forEach(el => {
            el.innerHTML = t(el.dataset.i18nHtml);
        });
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            el.placeholder = t(el.dataset.i18nPlaceholder);
        });
        document.querySelectorAll('[data-i18n-title]').forEach(el => {
            el.title = t(el.dataset.i18nTitle);
        });
        document.querySelectorAll('[data-i18n-value]').forEach(el => {
            el.value = t(el.dataset.i18nValue);
        });
        // Update page title
        const titleKey = 'page.title';
        const tr = t(titleKey);
        if (tr !== titleKey) document.title = tr;
    }

    /** Detect browser locale and resolve it. */
    function detectBrowser() {
        const langs = navigator.languages || [navigator.language || DEFAULT];
        for (const l of langs) {
            const r = resolve(l);
            if (r !== DEFAULT || l.startsWith('en')) return r;
        }
        return DEFAULT;
    }

    /**
     * Load locale from localStorage, optionally falling back to browser locale.
     * @param {boolean} [useBrowserDetect=false]
     *   true  → if no stored locale, detect from navigator.languages
     *   false → if no stored locale, default to 'en' (American English)
     */
    function init(useBrowserDetect) {
        let stored = null;
        try { stored = localStorage.getItem('locale'); } catch (_) {}
        if (stored) {
            _locale = resolve(stored);
        } else if (useBrowserDetect) {
            _locale = detectBrowser();
        } else {
            _locale = DEFAULT;   // American English
        }
        document.documentElement.lang = _locale;
    }

    return { t, setLocale, getLocale, supported, applyToDocument, init, resolve };
})();
